//! Shared JSON wire protocol types for hwLedger fleet management (FR-FLEET-001..008).
//!
//! Defines serializable types for agent registration, mTLS bootstrapping, heartbeat telemetry,
//! job dispatch, and state reporting. All types round-trip through `serde_json` for the HTTP/2 + JSON wire.

use serde::{Deserialize, Serialize};
use std::collections::BTreeMap;
use thiserror::Error;
use uuid::Uuid;

/// Error type for protocol violations and serialization failures.
#[derive(Debug, Error, Clone, Serialize, Deserialize, PartialEq)]
pub enum ProtoError {
    #[error("serialization error: {reason}")]
    Serialization { reason: String },

    #[error("validation error: {reason}")]
    Validation { reason: String },

    #[error("unsupported version: {version}")]
    UnsupportedVersion { version: String },
}

// ============================================================================
// Agent registration (FR-FLEET-001, FR-FLEET-002)
// ============================================================================

/// Platform metadata reported by an agent at registration.
/// Traces to: FR-FLEET-001
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct Platform {
    /// Operating system (e.g., "macos", "linux", "windows").
    pub os: String,
    /// CPU architecture (e.g., "aarch64", "x86_64").
    pub arch: String,
    /// Kernel version (e.g., "24.2.0" on macOS, "6.8.0" on Linux).
    pub kernel: String,
    /// Total system RAM in bytes.
    pub total_ram_bytes: u64,
    /// CPU model string (e.g., "Apple M4 Pro", "Intel Xeon").
    pub cpu_model: String,
}

/// Agent registration request, sent on first contact or when cert expires.
/// Bootstrap token is validated against server's configured list.
/// Traces to: FR-FLEET-001, FR-FLEET-002
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct AgentRegistration {
    /// Unique identifier for this agent (generated by agent via uuid::v4).
    pub agent_id: Uuid,
    /// Hostname of the agent's host machine.
    pub hostname: String,
    /// PEM-encoded PKCS#10 CSR to be signed by the server's CA.
    pub cert_csr_pem: String,
    /// Platform metadata.
    pub platform: Platform,
    /// One-time bootstrap token for validation.
    pub bootstrap_token: String,
}

/// Server's response to agent registration: includes signed cert and CA bundle.
/// Agent persists these to disk for future mTLS connections.
/// Traces to: FR-FLEET-001, FR-FLEET-002
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct RegistrationAck {
    /// The agent's UUID (echoed from request for confirmation).
    pub agent_id: Uuid,
    /// PEM-encoded certificate signed by the server's CA (30-day validity).
    pub assigned_cert_pem: String,
    /// PEM-encoded root CA certificate for client verification.
    pub ca_cert_pem: String,
    /// Server's current time in milliseconds since Unix epoch (for clock skew detection).
    pub server_time_ms: u64,
}

// ============================================================================
// Heartbeat and telemetry (FR-FLEET-001, FR-FLEET-002)
