/// Telemetry snapshot for a single device at a single instant.
/// Corresponds to `hwledger_probe::cache::Snapshot` but serializable.
/// Traces to: FR-TEL-002
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct TelemetrySnapshot {
    /// Free (unallocated) VRAM in bytes.
    pub free_vram_bytes: u64,
    /// Utilization percentage, 0.0–100.0.
    pub util_percent: f32,
    /// Device temperature in Celsius.
    pub temperature_c: f32,
    /// Power draw in watts.
    pub power_watts: f32,
    /// Capture timestamp in milliseconds since Unix epoch.
    pub captured_at_ms: u64,
}

/// Telemetry report for a single device.
/// Traces to: FR-TEL-002
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct DeviceReport {
    /// Probe backend name (e.g., "nvidia", "amd", "metal", "intel").
    pub backend: String,
    /// Device ID assigned by the backend.
    pub id: u32,
    /// Human-readable device name (e.g., "RTX 4090", "M4 Pro").
    pub name: String,
    /// Optional UUID for remote/fleet identification.
    pub uuid: Option<String>,
    /// Total VRAM in bytes.
    pub total_vram_bytes: u64,
    /// Latest telemetry snapshot (if available).
    pub snapshot: Option<TelemetrySnapshot>,
}

/// Heartbeat message: agent reports its status and device inventory.
/// Sent periodically (default 30s) to update the server's agent registry and device telemetry.
/// Traces to: FR-FLEET-001, FR-FLEET-002
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct Heartbeat {
    /// Agent's UUID (must match registered agent or registration is rejected).
    pub agent_id: Uuid,
    /// Uptime in seconds since agent start.
    pub uptime_s: u64,
    /// Enumerated devices with latest telemetry.
    pub devices: Vec<DeviceReport>,
}

// ============================================================================
// Job dispatch and reporting (FR-FLEET-007, FR-FLEET-008)
// ============================================================================

/// Job state machine.
/// Traces to: FR-FLEET-008
#[derive(Debug, Clone, Copy, Serialize, Deserialize, PartialEq, Eq, Hash)]
#[serde(rename_all = "PascalCase")]
pub enum JobState {
    /// Job has been created but not yet sent to an agent.
    Pending,
    /// Agent has acknowledged the job and is executing the command.
    Running,
    /// Command completed with exit code 0.
    Succeeded,
    /// Command completed with non-zero exit code or failed to start.
    Failed,
    /// Command did not complete before the deadline.
    TimedOut,
}

impl JobState {
    /// Returns true if the job is in a terminal state.
    pub fn is_terminal(&self) -> bool {
        matches!(self, JobState::Succeeded | JobState::Failed | JobState::TimedOut)
    }
}

/// Job dispatch order sent by the server to an agent.
/// Agent executes the command and reports back via DispatchReport.
/// Traces to: FR-FLEET-008
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct DispatchOrder {
    /// Unique job ID (assigned by server).
    pub job_id: Uuid,
    /// Target agent UUID.
    pub agent_id: Uuid,
    /// Model reference (e.g., "mistral-7b-inst-v0.3.gguf" or "Qwen3.6-A3B").
    pub model_ref: String,
    /// Optional backend hint (e.g., "metal" on Apple Silicon). Agent may override if unavailable.
    pub backend_hint: Option<String>,
    /// Command to execute (e.g., ["llama.cpp", "--model", "model.gguf", ...]).
    pub command: Vec<String>,
    /// Environment variables to pass to the command.
    pub env: BTreeMap<String, String>,
    /// Optional deadline in milliseconds since Unix epoch. Agent cancels if elapsed.
    pub deadline_ms: Option<u64>,
}

/// Job state report sent by agent to server when state changes.
/// Final report includes exit code and log tail for debugging.
/// Traces to: FR-FLEET-008
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct DispatchReport {
    /// Job ID (echoed from the DispatchOrder).
    pub job_id: Uuid,
    /// Agent UUID (echoed for routing).
    pub agent_id: Uuid,
    /// Current job state.
    pub state: JobState,
    /// Timestamp job transitioned to Running state (Unix epoch ms). None until Running.
    pub started_at_ms: Option<u64>,
    /// Timestamp job reached a terminal state (Unix epoch ms). None until terminal.
    pub finished_at_ms: Option<u64>,
    /// Exit code of the command (if terminal). None otherwise.
    pub exit_code: Option<i32>,
    /// Last 4 KB of process output (stdout + stderr interleaved if available).
    pub log_tail: String,
}

